#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{
    int a = 100;
    int b = 14;
    int c = 877;

    cout << hex;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;

    cout << oct;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;

    cout << dec;
    cout << a << endl;
    cout << b << endl;
    cout << c << endl;


    return 0;
}
